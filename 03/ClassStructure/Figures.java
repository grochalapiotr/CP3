public class Figures
{
    public static void rectangle(float x,float y){
        System.out.println(x*y);
    }
    
    public static void circle(float r){
        System.out.println(3.14 * r * r);
    }
    
    public static void triangle(float a,float h){
        System.out.println(a*h/2);
    }
}
